import whisper
import torch
import os

device = "cuda" if torch.cuda.is_available() else "cpu"
print(f"–ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —É—Å—Ç—Ä–æ–π—Å—Ç–≤–æ: {device}")

print("–ó–∞–≥—Ä—É–∂–∞–µ–º –º–æ–¥–µ–ª—å Whisper Large... (—ç—Ç–æ –∑–∞–π–º–µ—Ç –Ω–µ—Å–∫–æ–ª—å–∫–æ –º–∏–Ω—É—Ç)")
model = whisper.load_model("large", device=device)
print("‚úÖ –ú–æ–¥–µ–ª—å –∑–∞–≥—Ä—É–∂–µ–Ω–∞!")

# ============================================
# –ß–ê–°–¢–¨ 3: –£–ö–ê–ñ–ò–¢–ï –ò–ú–Ø –í–ê–®–ï–ì–û –§–ê–ô–õ–ê –ó–î–ï–°–¨
# ============================================
# –í–ê–ñ–ù–û: –°–Ω–∞—á–∞–ª–∞ –∑–∞–≥—Ä—É–∑–∏—Ç–µ —Ñ–∞–π–ª —á–µ—Ä–µ–∑ —Ñ–∞–π–ª–æ–≤—É—é —Å–∏—Å—Ç–µ–º—É Colab!
# 1. –í –ª–µ–≤–æ–π –ø–∞–Ω–µ–ª–∏ –Ω–∞–π–¥–∏—Ç–µ –∏–∫–æ–Ω–∫—É –ø–∞–ø–∫–∏ (Files) üìÅ
# 2. –ù–∞–∂–º–∏—Ç–µ Upload –∏–ª–∏ –ø–µ—Ä–µ—Ç–∞—â–∏—Ç–µ —Ñ–∞–π–ª
# 3. –£–∫–∞–∂–∏—Ç–µ –∏–º—è —Ñ–∞–π–ª–∞ –Ω–∏–∂–µ:

audio_file = "–∫—É—Ä—Å–æ—Ä.ogg"  # ‚¨ÖÔ∏è –ó–ê–ú–ï–ù–ò–¢–ï –ù–ê –ò–ú–Ø –í–ê–®–ï–ì–û –§–ê–ô–õ–ê!

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ñ–∞–π–ª–∞
if not os.path.exists(audio_file):
    print(f"‚ùå –§–∞–π–ª '{audio_file}' –Ω–µ –Ω–∞–π–¥–µ–Ω!")
    print("\nüìã –°–ø–∏—Å–æ–∫ —Ñ–∞–π–ª–æ–≤ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏:")
    all_files = [f for f in os.listdir('.') if os.path.isfile(f)]
    for f in all_files[:15]:
        print(f"  - {f}")
    print("\nüí° –£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ —Ñ–∞–π–ª –∑–∞–≥—Ä—É–∂–µ–Ω —á–µ—Ä–µ–∑ —Ñ–∞–π–ª–æ–≤—É—é —Å–∏—Å—Ç–µ–º—É Colab!")
else:
    file_size = os.path.getsize(audio_file) / 1024
    print(f"‚úÖ –§–∞–π–ª –Ω–∞–π–¥–µ–Ω: {audio_file} ({file_size:.2f} KB)")

# ============================================
# –ß–ê–°–¢–¨ 4: –¢—Ä–∞–Ω—Å–∫—Ä–∏–±–∞—Ü–∏—è
# ============================================
print(f"\nüé§ –ù–∞—á–∏–Ω–∞–µ–º —Ç—Ä–∞–Ω—Å–∫—Ä–∏–±–∞—Ü–∏—é: {audio_file}")
print("–≠—Ç–æ –º–æ–∂–µ—Ç –∑–∞–Ω—è—Ç—å –Ω–µ—Å–∫–æ–ª—å–∫–æ –º–∏–Ω—É—Ç...\n")

result = model.transcribe(
    audio_file,
    language="ru",  # "ru" - —Ä—É—Å—Å–∫–∏–π, "en" - –∞–Ω–≥–ª–∏–π—Å–∫–∏–π, None - –∞–≤—Ç–æ–æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ
    task="transcribe",  # "transcribe" - —Ç—Ä–∞–Ω—Å–∫—Ä–∏–±–∞—Ü–∏—è, "translate" - –ø–µ—Ä–µ–≤–æ–¥ –Ω–∞ –∞–Ω–≥–ª–∏–π—Å–∫–∏–π
    verbose=True,
    # –ü–∞—Ä–∞–º–µ—Ç—Ä—ã –¥–ª—è –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–µ–Ω–∏—è –ø—Ä–æ–ø—É—Å–∫–∞ —Ç–∏—Ö–∏—Ö —É—á–∞—Å—Ç–∫–æ–≤:
    no_speech_threshold=0.2,  # –°–Ω–∏–∂–∞–µ—Ç –ø–æ—Ä–æ–≥ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —Ç–∏—à–∏–Ω—ã (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 0.6) - –º–µ–Ω—å—à–µ –±—É–¥–µ—Ç –ø—Ä–æ–ø—É—Å–∫–∞—Ç—å
    condition_on_previous_text=False,  # –ù–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –ø—Ä–µ–¥—ã–¥—É—â–∏–π —Ç–µ–∫—Å—Ç - –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–∞–µ—Ç –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏—è
    compression_ratio_threshold=2.0,  # –ü–æ—Ä–æ–≥ —Å–∂–∞—Ç–∏—è - –º–µ–Ω—å—à–µ –ø—Ä–æ–ø—É—Å–∫–æ–≤
    logprob_threshold=-1.0,  # –ü–æ—Ä–æ–≥ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏ - —Ä–∞—Å–ø–æ–∑–Ω–∞–≤–∞—Ç—å –¥–∞–∂–µ —Ç–∏—Ö–∏–π –∑–≤—É–∫
)

# ============================================
# –ß–ê–°–¢–¨ 5: –í—ã–≤–æ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
# ============================================
print("\n" + "="*60)
print("–†–ï–ó–£–õ–¨–¢–ê–¢–´ –¢–†–ê–ù–°–ö–†–ò–ë–ê–¶–ò–ò")
print("="*60)

print("\nüìù –¢–ï–ö–°–¢:")
print(result["text"])

# ============================================
# –ß–ê–°–¢–¨ 6: –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
# ============================================
output_file = audio_file.rsplit('.', 1)[0] + "_transcription.txt"

with open(output_file, "w", encoding="utf-8") as f:
    f.write("–¢–†–ê–ù–°–ö–†–ò–ë–ê–¶–ò–Ø –ê–£–î–ò–û\n")
    f.write("="*60 + "\n\n")
    f.write(f"–§–∞–π–ª: {audio_file}\n")
    f.write(f"–Ø–∑—ã–∫: {result.get('language', '–∞–≤—Ç–æ–æ–ø—Ä–µ–¥–µ–ª–µ–Ω')}\n\n")
    f.write("–¢–ï–ö–°–¢:\n")
    f.write(result["text"])

print(f"\n‚úÖ –†–µ–∑—É–ª—å—Ç–∞—Ç —Å–æ—Ö—Ä–∞–Ω–µ–Ω –≤ —Ñ–∞–π–ª: {output_file}")

