# ============================================
"""
–ò–ù–°–¢–†–£–ö–¶–ò–Ø –ü–û –ó–ê–ì–†–£–ó–ö–ï –§–ê–ô–õ–ê:
1. –í –ª–µ–≤–æ–π –ø–∞–Ω–µ–ª–∏ Colab –Ω–∞–π–¥–∏—Ç–µ –∏–∫–æ–Ω–∫—É –ø–∞–ø–∫–∏ (Files) üìÅ
2. –ù–∞–∂–º–∏—Ç–µ –Ω–∞ –∏–∫–æ–Ω–∫—É –∑–∞–≥—Ä—É–∑–∫–∏ (Upload) –∏–ª–∏ –ø–µ—Ä–µ—Ç–∞—â–∏—Ç–µ —Ñ–∞–π–ª
3. –î–æ–∂–¥–∏—Ç–µ—Å—å –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –∑–∞–≥—Ä—É–∑–∫–∏ (—É–≤–∏–¥–∏—Ç–µ —Ñ–∞–π–ª –≤ —Å–ø–∏—Å–∫–µ)
4. –ó–∞–ø–æ–º–Ω–∏—Ç–µ —Ç–æ—á–Ω–æ–µ –∏–º—è —Ñ–∞–π–ª–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä: "–∫—É—Ä—Å–æ—Ä.ogg")
5. –£–∫–∞–∂–∏—Ç–µ –∏–º—è —Ñ–∞–π–ª–∞ –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π audio_file –Ω–∏–∂–µ
"""

# ‚¨áÔ∏è –ó–î–ï–°–¨ –£–ö–ê–ñ–ò–¢–ï –ò–ú–Ø –í–ê–®–ï–ì–û –§–ê–ô–õ–ê ‚¨áÔ∏è
audio_file = "–∫—É—Ä—Å–æ—Ä.ogg"  # –ó–∞–º–µ–Ω–∏—Ç–µ –Ω–∞ –∏–º—è –≤–∞—à–µ–≥–æ —Ñ–∞–π–ª–∞

# –ü—Ä–æ–≤–µ—Ä–∫–∞, —á—Ç–æ —Ñ–∞–π–ª —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
if os.path.exists(audio_file):
    file_size = os.path.getsize(audio_file) / 1024
    print(f"‚úÖ –§–∞–π–ª –Ω–∞–π–¥–µ–Ω: {audio_file}")
    print(f"üìä –†–∞–∑–º–µ—Ä: {file_size:.2f} KB")
else:
    print(f"‚ùå –§–∞–π–ª '{audio_file}' –Ω–µ –Ω–∞–π–¥–µ–Ω!")
    print("\nüìã –°–ø–∏—Å–æ–∫ –≤—Å–µ—Ö —Ñ–∞–π–ª–æ–≤:")
    all_files = [f for f in os.listdir('.') if os.path.isfile(f)]
    for f in all_files[:10]:  # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–µ—Ä–≤—ã–µ 10 —Ñ–∞–π–ª–æ–≤
        print(f"  - {f}")
    print("\nüí° –£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ —Ñ–∞–π–ª –∑–∞–≥—Ä—É–∂–µ–Ω —á–µ—Ä–µ–∑ —Ñ–∞–π–ª–æ–≤—É—é —Å–∏—Å—Ç–µ–º—É Colab!")


# ============================================
# –Ø–ß–ï–ô–ö–ê 4: –¢—Ä–∞–Ω—Å–∫—Ä–∏–±–∞—Ü–∏—è –∞—É–¥–∏–æ
